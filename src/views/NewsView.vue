<template>
  <div>
    <div v-for="user in users" v-bind:key="user.data">{{user.title}}</div>
  </div>
</template>

<script>
import axios from 'axios';
export default {
  data() {
    return {
      users: [],
    }
  },
  created() {
    const vm = this;
    axios.get('https://api.hnpwa.com/v0/news/1.json')
    .then(function(response) {
      vm.users = response.data;
    })
    .catch(function(error) {
      console.log(error);
    })
  }
}
</script>

